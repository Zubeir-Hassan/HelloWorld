#include <iostream>
using namespace std;

//Declare the variables
int main(){
    int base =10;
    int height =7;
    int hypotenuse=12;
//Declare the area of a Triangle
    int area;
    area=0.5*base*height;
    int perimeter;
    perimeter=base+height+hypotenuse;
    cout<<"Area of a right angeled triangle is ="<<area<<endl;
    cout<<"Perimeter of a right angeled triangle is ="<<perimeter<<endl;
    return 0;

}